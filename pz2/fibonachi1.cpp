#include <iostream>

using namespace std;

int main()
{
    double n, b, a,c;

    cout << "n=" << endl;
    cin >> n;

    b = 1;
    a = 0;

    cout << 0<<" ";
    cout << 1<<" ";

    for (int i = 1; i <= (n-1); i++) // задаем начальное значение 1, конечное 1000 и задаем шаг цикла - 1.
    {
        
        c = a + b;
        cout << c<<" ";
        a = b;
        b = c;
    }
}

